program hello
print*, "Hello world!"
!Stop 
print '(A)', "Hello, world!"; PRINT*, "Hola mundo!"
print '(A20)', 'Hello                                                                                                             &
  '
STOP "Goodbye world" 
end program hello

!En Fortran, la línea `print*, "Hello world!"` se utiliza para imprimir el texto
! `"Hello world!"` en la salida estándar, que normalmente es la pantalla de la consola.

!Desglosando la línea:

!- `print*`: El comando `print` en Fortran se usa para mostrar datos en la salida estándar. 
!El asterisco (`*`) es una forma de especificar que el formato de salida debe ser el formato estándar predeterminado. 
!En otras palabras, el asterisco indica que Fortran debe usar un formato automático para la impresión de datos.

!- `"Hello world!"`: Es una cadena de texto que se quiere imprimir.

!Entonces, cuando esta línea de código se ejecuta, Fortran mostrará el mensaje `"Hello world!"` en la consola o terminal desde la cual se está ejecutando el programa.

!Esta es una manera básica de mostrar un mensaje en la salida estándar, útil para pruebas y depuración en programas Fortran.
